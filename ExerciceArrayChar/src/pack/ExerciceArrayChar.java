package pack;
import java.util.Arrays;

public class ExerciceArrayChar {

	public static void main(String[] args) {
		char[] tc = {'a','b','c'};
		System.out.println(tc);
		System.out.println("Voici le tableau tc: " + tc);
		System.out.print("Voici le tableau tc: ");
		System.out.println(tc.toString());
		
		
		//Solution to print the table
		System.out.println(Arrays.toString(tc));
		System.out.println("Voici le tableau tc: " + Arrays.toString(tc));
	}

}
